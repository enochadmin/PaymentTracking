<script>
    function downloadPDF() {
        const element = document.querySelector('table');
        const opt = {
            margin: 0.5,
            filename: 'projects_list.pdf',
            image: { type: 'jpeg', quality: 0.98 },
            html2canvas: { scale: 2 },
            jsPDF: { unit: 'in', format: 'letter', orientation: 'landscape' }
        };
        html2pdf().set(opt).from(element).save();
    }
